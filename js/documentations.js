$(document).ready(function()
{
     console.log("Le document est prêt");
     $('.1').click(function()
		{
		console.log("Le bouton a été cliqué.");
        if ($('.1').val()==='cacher')
        {
            $('#masquer1').fadeOut();
            $('.1').val('montrer')
		}
        else
        {
            console.log("Le bontoun a été cliqué.");
            $('#masquer1').show(500);
            $('.1').val('cacher')
        }
    })
});

